/// String containing ISO 4217 currency code, e.g. "USD", "EUR", "JPY".
typedef CurrencyCode = String;

/// Collection of commonly used ISO 4217 currency codes.
abstract final class CurrencyCodes {
  /// AED - United Arab Emirates Dirham
  static const CurrencyCode aed = 'AED';

  /// AUD - Australian Dollar
  static const CurrencyCode aud = 'AUD';

  /// CAD - Canadian Dollar
  static const CurrencyCode cad = 'CAD';

  /// CHF - Swiss Franc
  static const CurrencyCode chf = 'CHF';

  /// CNY - Chinese Yuan Renminbi
  static const CurrencyCode cny = 'CNY';

  /// EUR - Euro
  static const CurrencyCode eur = 'EUR';

  /// GBP - British Pound Sterling
  static const CurrencyCode gbp = 'GBP';

  /// HKD - Hong Kong Dollar
  static const CurrencyCode hkd = 'HKD';

  /// INR - Indian Rupee
  static const CurrencyCode inr = 'INR';

  /// JPY - Japanese Yen
  static const CurrencyCode jpy = 'JPY';

  /// PLN - Polish Zloty
  static const CurrencyCode pln = 'PLN';

  /// SGD - Singapore Dollar
  static const CurrencyCode sgd = 'SGD';

  /// USD - United States Dollar
  static const CurrencyCode usd = 'USD';

  /// ZAR - South African Rand
  static const CurrencyCode zar = 'ZAR';
}
